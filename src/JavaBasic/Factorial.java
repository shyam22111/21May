package JavaBasic;

public class Factorial {
	
	public static void main(String[] args) {
		
		
		 
			   int i,fact=1;  
			     int number=5;
			     
			       for(i=1;i<=number;i++){    
			             fact=fact*i;    //1*1 //2*3//6*4//24*5//120
			               }    
			                 System.out.println("Factorial of "+number+" is: "+fact);    
			                  }  
		
		
	}

