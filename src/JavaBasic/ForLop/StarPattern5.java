package JavaBasic.ForLop;

public class StarPattern5 
{

	public static void main(String[] args)
	{
		
		  
			
			for(int a=1; a<=10;a++) 
			{
				System.out.print("*");
				
			} System.out.println();
			
			for(int b=1; b<=10;b++) 
			{
				System.out.println("*");
  		     	
				
			}
			for(int c=1; c<=10;c++) 
			{
				System.out.print("*");
				
			} System.out.println();
			
	
		
			
			
			for(int d=1; d<=10; d++)
			{    
				for(int e=1; e<=10;e++)
				{

					
					  if(d>=1 && e==10 )
					  {
						  
						  System.out.print("*");
					  }
					  else {
						  System.out.print(" ");
					  }
				}System.out.println();
				
			}  
			for(int f= 1; f<=10; f++)
			{
				System.out.print("*");
			}
			
			
		
		
		
	}

}
